def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--env-name")
    parser.add_argument("--seed", type=int, default=0)
    parser.add_argument("--gpu", type=int, default=0)
    parser.add_argument("--output", type=Path, default=DEFAULT_OUTPUT)
    parser.add_argument(
        "--base-output", type=Path, default=DEFAULT_BASE_OUTPUT
    )
    parser.add_argument("--aggregate", action="store_true")
    parser.add_argument("--overwrite", action="store_true")
    parser.add_argument("--smoke", action="store_true")
    parser.add_argument("--smoke-queries", type=int, default=192)
    parser.add_argument("--batch-size", type=int, default=512)
    parser.add_argument("--max-events", type=int, default=8)
    parser.add_argument("--conditional-hidden", type=int, default=192)
    parser.add_argument("--conditional-epochs", type=int, default=30)
    parser.add_argument("--conditional-patience", type=int, default=7)
    parser.add_argument("--conditional-lr", type=float, default=4e-4)
    parser.add_argument("--bootstrap-repetitions", type=int, default=2000)
    args = parser.parse_args()
    for name in ("output", "base_output"):
        value = getattr(args, name)
        if not value.is_absolute():
            setattr(args, name, ROOT / value)
    if args.gpu == 3 or args.gpu not in (0, 1, 2):
        parser.error("--gpu must be one of 0,1,2; GPU3 is prohibited")
    if args.smoke:
        args.conditional_epochs = min(args.conditional_epochs, 3)
        args.conditional_patience = min(args.conditional_patience, 2)
        args.bootstrap_repetitions = min(args.bootstrap_repetitions, 100)
    return args


def main() -> None:
    args = parse_args()
    if args.aggregate:
        aggregate(args)
        return
    if args.env_name not in FOCUSED_ENVS:
        raise ValueError(
            f"--env-name must be one of {FOCUSED_ENVS}; received {args.env_name}"
        )
    run_cell(args)


if __name__ == "__main__":
    main()
