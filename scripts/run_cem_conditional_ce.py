def sample_episode_indices(
    episode_ids: np.ndarray,
    rng: np.random.Generator,
) -> np.ndarray:
    unique = np.unique(episode_ids)
    sampled = rng.choice(unique, size=len(unique), replace=True)
    chunks = [np.flatnonzero(episode_ids == episode) for episode in sampled]
    return np.concatenate(chunks) if chunks else np.asarray([], dtype=np.int64)


def metric_from_sample(
    arrays: dict[str, np.ndarray],
    method: str,
    metric: str,
    index: np.ndarray,
) -> float:
    if metric == "spearman":
        prediction = arrays[
            "conditional_prediction"
            if method == "conditional"
            else "singleton_prediction"
        ][index]
        target = arrays["conditional_target"][index]
        valid = arrays["candidate"][index]
        value = centered_rank_correlation(prediction, target, valid)
        return float(value) if value is not None else float("nan")
    if metric == "pairwise":
        correct = arrays[f"{method}_pair_correct"][index].sum()
        count = arrays[f"{method}_pair_count"][index].sum()
        return float(correct / count) if count else float("nan")
    if metric == "deletion_gap":
        values = arrays[f"{method}_deletion_gap"][index]
        return (
            float(np.nanmean(values))
            if np.isfinite(values).any()
            else float("nan")
        )
    raise KeyError(metric)


def bootstrap_environment(
    cells: list[dict[str, Any]],
    method: str,
    metric: str,
    seed: int,
    repetitions: int,
) -> dict[str, Any]:
    loaded = []
    for cell in cells:
        path = ROOT / cell["artifacts"]["evaluation"]
        with np.load(path, allow_pickle=False) as data:
            loaded.append({key: np.asarray(data[key]) for key in data.files})
    point_values = []
    for arrays in loaded:
        index = np.arange(len(arrays["episode_ids"]))
        point_values.append(metric_from_sample(arrays, method, metric, index))
    point = float(np.nanmean(point_values))
    rng = np.random.default_rng(seed)
    draws = []
    for _ in range(repetitions):
        seed_indices = rng.choice(
            len(loaded), size=len(loaded), replace=True
        )
        values = []
        for seed_index in seed_indices:
            arrays = loaded[int(seed_index)]
            index = sample_episode_indices(arrays["episode_ids"], rng)
            values.append(metric_from_sample(arrays, method, metric, index))
        draws.append(float(np.nanmean(values)))
    draws_np = np.asarray(draws, dtype=np.float64)
    draws_np = draws_np[np.isfinite(draws_np)]
    return {
        "mean": point,
        "ci95": (
            np.quantile(draws_np, [0.025, 0.975]).astype(float).tolist()
            if len(draws_np)
            else [None, None]
        ),
        "seed_values": [float(value) for value in point_values],
        "seed_count": len(loaded),
        "bootstrap_unit": "optimization seed, then trajectory within seed",
        "_draws": draws_np,
    }


def aggregate(args: argparse.Namespace) -> dict[str, Any]:
    cells = []
    for path in sorted((args.output / "cells").glob("*/*/result.json")):
        result = json.loads(path.read_text())
        if result.get("schema") == "graph_cem_conditional_cell_v1":
            cells.append(result)
    grouped: dict[str, list[dict[str, Any]]] = {}
    for cell in cells:
        grouped.setdefault(cell["environment"], []).append(cell)
    environments = []
    global_draws: dict[tuple[str, str], list[np.ndarray]] = {}
    for env_index, (environment, rows) in enumerate(sorted(grouped.items())):
        methods = {}
        for method in ("conditional", "singleton"):
            methods[method] = {}
            for metric_index, metric in enumerate(
                ("spearman", "pairwise", "deletion_gap")
            ):
                summary = bootstrap_environment(
                    rows,
                    method,
                    metric,
                    seed=71_000 + env_index * 101 + metric_index * 17,
                    repetitions=args.bootstrap_repetitions,
                )
                global_draws.setdefault((method, metric), []).append(
                    summary.pop("_draws")
                )
                methods[method][metric] = summary
        environments.append(
            {
                "environment": environment,
                "family": rows[0]["family"],
                "seeds": sorted(int(row["seed"]) for row in rows),
                "methods": methods,
                "conditional_candidate_groups": int(
                    sum(
                        row["target_telemetry"]["test"]["candidate_groups"]
                        for row in rows
                    )
                ),
                "conditional_candidate_pairs": int(
                    sum(
                        row["target_telemetry"]["test"]["candidate_pairs"]
                        for row in rows
                    )
                ),
                "dependency_payload_leaks": int(
                    sum(
                        row["target_telemetry"]["test"][
                            "dependency_payload_leaks_across_deletions"
                        ]
                        for row in rows
                    )
                ),
            }
        )
    aggregate_metrics = {}
    for method in ("conditional", "singleton"):
        aggregate_metrics[method] = {}
        for metric in ("spearman", "pairwise", "deletion_gap"):
            env_summaries = [
                environment["methods"][method][metric]
                for environment in environments
            ]
            point = (
                float(np.mean([summary["mean"] for summary in env_summaries]))
                if env_summaries
                else None
            )
            draws_by_env = global_draws.get((method, metric), [])
            if draws_by_env:
                length = min(len(values) for values in draws_by_env)
                stacked = np.stack(
                    [values[:length] for values in draws_by_env]
                )
                global_values = stacked.mean(0)
                ci = np.quantile(
                    global_values, [0.025, 0.975]
                ).astype(float).tolist()
            else:
                ci = [None, None]
            aggregate_metrics[method][metric] = {
                "mean": point,
                "ci95": ci,
                "hierarchy": (
                    "environment mean of seed/trajectory nested bootstrap"
                ),
            }
    conditional = aggregate_metrics.get("conditional", {})
    spearman = conditional.get("spearman", {})
    pairwise = conditional.get("pairwise", {})
    positive_deletion_envs = [
        environment["environment"]
        for environment in environments
        if (
            environment["methods"]["conditional"]["deletion_gap"]["ci95"][0]
            is not None
            and environment["methods"]["conditional"]["deletion_gap"][
                "ci95"
            ][0]
            > 0
        )
    ]
    rank_pass = bool(
        (
            spearman.get("ci95", [None])[0] is not None
            and spearman["ci95"][0] > 0.2
        )
        or (
            pairwise.get("mean") is not None
            and pairwise["mean"] >= 0.65
        )
    )
    deletion_pass = len(positive_deletion_envs) >= 2
