    result_path = output_dir / "result.json"
    if result_path.is_file() and not args.overwrite:
        return json.loads(result_path.read_text())
    output_dir.mkdir(parents=True, exist_ok=True)
    started = time.time()
    set_seed(args.seed)
    device = resolve_device(args.gpu)
    (
        host,
        memory,
        base_config,
        tensors,
        query_sets,
        provenance,
    ) = prepare_base_model(args, device, output_dir)
    config = diagnostic_config(base_config, args.max_events)
    targets = {}
    features = {}
    for split in ("train", "validation", "test"):
        targets[split] = compute_conditional_targets(
            host,
            memory,
            tensors[split],
            query_sets[split],
            config,
            args.batch_size,
        )
        features[split] = build_ce_features(
            memory, tensors[split], targets[split].candidate
        )
    head = ConditionalCEHead(
        features["train"].item.shape[-1],
        hidden=args.conditional_hidden,
    ).to(device)
    training = train_conditional_head(
        head,
        features["train"],
        targets["train"],
        features["validation"],
        targets["validation"],
        args,
    )
    with torch.no_grad():
        conditional_prediction, log_variance, hidden = head(
            features["test"].item
        )
        pair_prediction, _ = head.pair(hidden)
        singleton_prediction = memory.query_ce_hat(
            tensors["test"].context_z,
            tensors["test"].future_actions[:, 0],
            tensors["test"].events,
            tensors["test"].metadata,
        )
        singleton_target, _, _ = compute_group_ce_targets(
            host, memory, tensors["test"], args.batch_size
        )
    candidate = targets["test"].candidate.detach().cpu().numpy()
    conditional_truth = targets["test"].singleton.detach().cpu().numpy()
    conditional_np = conditional_prediction.detach().cpu().numpy()
    singleton_np = singleton_prediction.detach().cpu().numpy()
    singleton_truth_np = singleton_target.detach().cpu().numpy()
