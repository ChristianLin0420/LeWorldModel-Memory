@dataclass
class ReplayScores:
    ce_hat: torch.Tensor
    route: torch.Tensor
    combined: torch.Tensor


@torch.no_grad()
def store_scores(
    memory: RawMemoryConditioner,
    batch: QueryTensors,
) -> ReplayScores:
    ce_hat = memory.query_ce_hat(
        batch.context_z,
        batch.future_actions[:, 0],
        batch.events,
        batch.metadata,
    )
    route = memory.scores(
        batch.context_z,
        batch.future_actions[:, 0],
        batch.events,
        batch.metadata,
    )
    route_mean = (
        route.masked_fill(~batch.valid, 0.0).sum(1, keepdim=True)
        / batch.valid.sum(1, keepdim=True).clamp_min(1)
    )
    return ReplayScores(
        ce_hat=ce_hat,
        route=route,
        combined=ce_hat + 0.01 * (route - route_mean),
    )


@torch.no_grad()
def replay_store_masks(
    batch: QueryTensors,
    queries: list[QuerySample],
    config: StoreConfig,
    scores: ReplayScores,
    excluded: torch.Tensor | None = None,
) -> tuple[torch.Tensor, torch.Tensor, dict[str, Any]]:
    """Rebuild the version store and reroute after dependency-closed deletion."""
    if excluded is None:
        excluded = torch.zeros_like(batch.valid)
    if excluded.shape != batch.valid.shape:
        raise ValueError("excluded mask shape does not match query store")
    selected = torch.zeros_like(batch.valid)
    occupied = torch.zeros_like(batch.valid)
    fallback_after_deletion = 0
    selected_deleted_payloads = 0
    occupancies = []
    for row, query in enumerate(queries):
        events = ordered_query_events(query, batch.events.shape[1])
        store = VersionedEventStore(
            budget=int(config.budget),
            hysteresis=float(config.hysteresis),
        )
        excluded_keys = {
            events[column].key_id
            for column in range(len(events))
            if bool(excluded[row, column])
        }
        for column, event in enumerate(events):
            if bool(excluded[row, column]):
                continue
            store.propose(
                event_id=event.event_id,
                key_id=event.key_id,
                event_timestamp=event.peak_t,
                proposed_at=event.start,
            )
            verified_at = event.end + int(config.verification_delay)
            if verified_at > query.query_t:
                continue
            store.verify(
                event_id=event.event_id,
                verified_at=verified_at,
                ce_hat=float(scores.ce_hat[row, column]),
                threshold=float(config.promotion_threshold),
            )
        active_ids = {record.event_id for record in store.active()}
        active_columns = [
            column
            for column, event in enumerate(events)
            if event.event_id in active_ids
        ]
        if excluded_keys:
            fallback_after_deletion += sum(
                events[column].key_id in excluded_keys
                for column in active_columns
            )
        if active_columns:
            occupied[row, active_columns] = True
            ranking = sorted(
                active_columns,
                key=lambda column: float(scores.combined[row, column]),
                reverse=True,
            )
            recalled = ranking[: min(int(config.topk), len(ranking))]
            selected[row, recalled] = True
        occupancies.append(len(active_columns))
        selected_deleted_payloads += int(bool((selected[row] & excluded[row]).any()))
    occupancy = np.asarray(occupancies, dtype=np.float64)
    telemetry = {
        "mean_occupancy": float(occupancy.mean()) if len(occupancy) else 0.0,
        "maximum_occupancy": int(occupancy.max(initial=0)),
        "multi_group_query_count": int((occupied.sum(1) >= 2).sum()),
        "fallback_versions_after_deletion": int(fallback_after_deletion),
        "selected_deleted_payloads": int(selected_deleted_payloads),
        "dependency_payload_leaks": int(selected_deleted_payloads),
        "router_rerun_after_deletion": True,
    }
    return selected, occupied, telemetry


def normalized_conditional_effect(
    deleted_loss: torch.Tensor,
    full_loss: torch.Tensor,
    empty_loss: torch.Tensor,
) -> torch.Tensor:
    return (deleted_loss - full_loss) / empty_loss.clamp_min(1e-8)


@dataclass
class ConditionalTargets:
    singleton: torch.Tensor
    pair: torch.Tensor
    candidate: torch.Tensor
    pair_valid: torch.Tensor
    full_mask: torch.Tensor
    full_loss: torch.Tensor
    empty_loss: torch.Tensor
    telemetry: dict[str, Any]


@torch.no_grad()
def per_query_loss(
    host: ActionConditionedHost,
    memory: RawMemoryConditioner | None,
    batch: QueryTensors,
    mask: torch.Tensor | None = None,
    batch_size: int = 512,
) -> torch.Tensor:
    values = []
    for start in range(0, len(batch), batch_size):
        stop = min(len(batch), start + batch_size)
        index = torch.arange(start, stop, device=batch.context_z.device)
        part = batch.index(index)
        part_mask = None if mask is None else mask[start:stop]
        prediction, _ = rollout(host, memory, part, mask=part_mask)
        values.append(horizon_loss(prediction, part.targets).mean(1))
    return torch.cat(values)


@torch.no_grad()
def compute_conditional_targets(
    host: ActionConditionedHost,
    memory: RawMemoryConditioner,
    batch: QueryTensors,
    queries: list[QuerySample],
    config: StoreConfig,
    batch_size: int,
) -> ConditionalTargets:
    scores = store_scores(memory, batch)
    full_mask, occupied, full_telemetry = replay_store_masks(
        batch, queries, config, scores
    )
    full_loss = per_query_loss(
        host, memory, batch, full_mask, batch_size=batch_size
    )
    empty_loss = per_query_loss(
        host, None, batch, None, batch_size=batch_size
    )
    rows, slots = batch.valid.shape
    singleton = torch.zeros((rows, slots), device=batch.valid.device)
    pair = torch.zeros((rows, slots, slots), device=batch.valid.device)
    fallback_count = 0
    leak_count = 0
    for first in range(slots):
        excluded = torch.zeros_like(batch.valid)
        excluded[:, first] = occupied[:, first]
        deleted_mask, _, telemetry = replay_store_masks(
            batch, queries, config, scores, excluded
        )
        deleted_loss = per_query_loss(
            host, memory, batch, deleted_mask, batch_size=batch_size
        )
        singleton[:, first] = normalized_conditional_effect(
            deleted_loss, full_loss, empty_loss
        )
        fallback_count += telemetry["fallback_versions_after_deletion"]
        leak_count += telemetry["dependency_payload_leaks"]
    upper = torch.triu(
        torch.ones((slots, slots), dtype=torch.bool, device=occupied.device),
        diagonal=1,
    )
    pair_valid = (
        occupied.unsqueeze(2) & occupied.unsqueeze(1) & upper.unsqueeze(0)
    )
    for first in range(slots):
        for second in range(first + 1, slots):
            eligible = occupied[:, first] & occupied[:, second]
            if not bool(eligible.any()):
                continue
            excluded = torch.zeros_like(batch.valid)
            excluded[:, first] = eligible
            excluded[:, second] = eligible
            deleted_mask, _, telemetry = replay_store_masks(
                batch, queries, config, scores, excluded
            )
            deleted_loss = per_query_loss(
                host, memory, batch, deleted_mask, batch_size=batch_size
            )
            target = normalized_conditional_effect(
                deleted_loss, full_loss, empty_loss
            )
            pair[:, first, second] = target
            pair[:, second, first] = target
            fallback_count += telemetry["fallback_versions_after_deletion"]
            leak_count += telemetry["dependency_payload_leaks"]
    singleton = singleton.masked_fill(~occupied, 0.0)
    telemetry = {
        **full_telemetry,
        "candidate_groups": int(occupied.sum()),
        "candidate_pairs": int(pair_valid.sum()),
        "full_store_retrieval_rate": float(full_mask.any(1).float().mean()),
        "fallback_versions_across_deletions": int(fallback_count),
        "dependency_payload_leaks_across_deletions": int(leak_count),
        "target": (
            "[L(reroute(rebuild(M without G)))-L(route(M))]/"
            "[L(empty)+1e-8]"
        ),
        "pair_deletions_included": True,
    }
    return ConditionalTargets(
        singleton=singleton,
        pair=pair,
        candidate=occupied,
        pair_valid=pair_valid,
        full_mask=full_mask,
        full_loss=full_loss,
        empty_loss=empty_loss,
        telemetry=telemetry,
    )


@dataclass
class CEFeatures:
    item: torch.Tensor
    candidate: torch.Tensor

    def index(self, index: torch.Tensor) -> "CEFeatures":
        return CEFeatures(item=self.item[index], candidate=self.candidate[index])


@torch.no_grad()
def build_ce_features(
    memory: RawMemoryConditioner,
    batch: QueryTensors,
    candidate: torch.Tensor,
) -> CEFeatures:
    query = memory.query_vector(
        batch.context_z, batch.future_actions[:, 0]
    )
    scores = store_scores(memory, batch)
    count = candidate.sum(1, keepdim=True).clamp_min(1)
    store_mean = (
        batch.events * candidate.unsqueeze(-1)
    ).sum(1) / count
    query_expand = query[:, None].expand(-1, batch.events.shape[1], -1)
    mean_expand = store_mean[:, None].expand_as(batch.events)
    occupancy = (
        candidate.sum(1, keepdim=True).float() / batch.events.shape[1]
    )[:, None].expand(-1, batch.events.shape[1], -1)
    centered_route = scores.route - (
        scores.route.masked_fill(~batch.valid, 0.0).sum(1, keepdim=True)
        / batch.valid.sum(1, keepdim=True).clamp_min(1)
    )
    item = torch.cat(
        [
            batch.events,
            query_expand,
            mean_expand,
            batch.events - mean_expand,
            batch.metadata,
            scores.ce_hat.unsqueeze(-1),
            centered_route.unsqueeze(-1),
            occupancy,
        ],
        dim=-1,
    ).detach()
    return CEFeatures(item=item, candidate=candidate)


class ConditionalCEHead(nn.Module):
    """Heteroscedastic item and symmetric pair conditional-effect predictor."""

    def __init__(self, input_dim: int, hidden: int = 192) -> None:
        super().__init__()
        self.encoder = nn.Sequential(
            nn.LayerNorm(input_dim),
            nn.Linear(input_dim, hidden),
            nn.GELU(),
            nn.Linear(hidden, hidden),
            nn.GELU(),
        )
        self.item_output = nn.Linear(hidden, 2)
        self.pair_output = nn.Sequential(
            nn.LayerNorm(3 * hidden),
            nn.Linear(3 * hidden, hidden),
            nn.GELU(),
            nn.Linear(hidden, 2),
        )

    def forward(
        self, item: torch.Tensor
    ) -> tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
        hidden = self.encoder(item)
        output = self.item_output(hidden)
        return output[..., 0], output[..., 1].clamp(-8.0, 3.0), hidden

    def pair(
        self, hidden: torch.Tensor
    ) -> tuple[torch.Tensor, torch.Tensor]:
        left = hidden.unsqueeze(2)
        right = hidden.unsqueeze(1)
        symmetric = torch.cat(
            [left + right, (left - right).abs(), left * right], dim=-1
        )
        output = self.pair_output(symmetric)
        return output[..., 0], output[..., 1].clamp(-8.0, 3.0)


def heteroscedastic_loss(
    mean: torch.Tensor,
    log_variance: torch.Tensor,
    target: torch.Tensor,
) -> torch.Tensor:
    squared = torch.square(mean - target)
    return 0.5 * (torch.exp(-log_variance) * squared + log_variance)


def within_query_losses(
    prediction: torch.Tensor,
    target: torch.Tensor,
    valid: torch.Tensor,
    temperature: float,
) -> tuple[torch.Tensor, torch.Tensor]:
    true_delta = target.unsqueeze(2) - target.unsqueeze(1)
    pred_delta = prediction.unsqueeze(2) - prediction.unsqueeze(1)
    slots = prediction.shape[1]
    upper = torch.triu(
        torch.ones((slots, slots), dtype=torch.bool, device=prediction.device),
        diagonal=1,
    )
    pair_mask = (
        valid.unsqueeze(2)
        & valid.unsqueeze(1)
        & upper.unsqueeze(0)
        & (true_delta.abs() > 1e-6)
    )
    ranking = (
        F.softplus(
            -true_delta.sign() * pred_delta / max(temperature, 1e-4)
        )[pair_mask].mean()
        if bool(pair_mask.any())
        else prediction.new_zeros(())
    )
    eligible = valid.sum(1) >= 2
    if bool(eligible.any()):
        logits = (prediction / max(temperature, 1e-4)).masked_fill(
            ~valid, -1e9
        )
        truth = (target / max(temperature, 1e-4)).masked_fill(~valid, -1e9)
        target_probability = torch.softmax(truth[eligible], dim=1).detach()
        listwise = -(
            target_probability * torch.log_softmax(logits[eligible], dim=1)
        ).sum(1).mean()
    else:
        listwise = prediction.new_zeros(())
    return ranking, listwise


def train_conditional_head(
    head: ConditionalCEHead,
    train_features: CEFeatures,
    train_targets: ConditionalTargets,
    validation_features: CEFeatures,
    validation_targets: ConditionalTargets,
    args: argparse.Namespace,
) -> dict[str, Any]:
    optimizer = torch.optim.AdamW(
        head.parameters(), lr=args.conditional_lr, weight_decay=1e-4
    )
    rng = np.random.default_rng(args.seed + 31_337)
    target_values = train_targets.singleton[train_targets.candidate]
    temperature = max(
        1e-3, float(target_values.detach().float().std().cpu())
    )
    best_objective = float("inf")
    best_state = None
    best_epoch = 0
    stale = 0
    history = []
    for epoch in range(args.conditional_epochs):
        head.train()
        losses = []
        for indices in batches(len(train_features.item), args.batch_size, rng):
            index = torch.as_tensor(
                indices, device=train_features.item.device
            )
            features = train_features.index(index)
            truth = train_targets.singleton[index]
            pair_truth = train_targets.pair[index]
            pair_valid = train_targets.pair_valid[index]
            mean, log_variance, hidden = head(features.item)
            valid = features.candidate
            regression = heteroscedastic_loss(
                mean[valid], log_variance[valid], truth[valid]
            ).mean()
            calibration = F.smooth_l1_loss(
                mean[valid], truth[valid], beta=max(temperature, 1e-3)
            )
            ranking, listwise = within_query_losses(
                mean, truth, valid, temperature
            )
            pair_mean, pair_log_variance = head.pair(hidden)
            pair_regression = (
                heteroscedastic_loss(
                    pair_mean[pair_valid],
                    pair_log_variance[pair_valid],
                    pair_truth[pair_valid],
                ).mean()
                if bool(pair_valid.any())
                else regression.new_zeros(())
            )
            loss = (
                regression
                + 0.25 * calibration
                + ranking
                + 0.25 * listwise
                + 0.5 * pair_regression
            )
            optimizer.zero_grad(set_to_none=True)
            loss.backward()
            nn.utils.clip_grad_norm_(head.parameters(), 1.0)
            optimizer.step()
            losses.append(float(loss.detach()))
        head.eval()
        with torch.no_grad():
            val_mean, val_log_variance, val_hidden = head(
                validation_features.item
            )
            valid = validation_features.candidate
            val_nll = heteroscedastic_loss(
                val_mean[valid],
                val_log_variance[valid],
                validation_targets.singleton[valid],
            ).mean()
            val_rank, val_listwise = within_query_losses(
                val_mean,
                validation_targets.singleton,
                valid,
                temperature,
            )
            pair_mean, pair_log_variance = head.pair(val_hidden)
            pair_valid = validation_targets.pair_valid
            pair_nll = (
                heteroscedastic_loss(
                    pair_mean[pair_valid],
                    pair_log_variance[pair_valid],
                    validation_targets.pair[pair_valid],
                ).mean()
                if bool(pair_valid.any())
                else val_nll.new_zeros(())
            )
            objective = float(
                val_nll + val_rank + 0.25 * val_listwise + 0.5 * pair_nll
            )
        history.append(
            {
                "epoch": epoch + 1,
                "train_objective": float(np.mean(losses)),
                "validation_objective": objective,
                "validation_nll": float(val_nll),
                "validation_pair_nll": float(pair_nll),
            }
        )
        if math.isfinite(objective) and objective < best_objective - 1e-6:
            best_objective = objective
            best_epoch = epoch + 1
            best_state = {
                key: value.detach().cpu().clone()
                for key, value in head.state_dict().items()
            }
            stale = 0
        else:
            stale += 1
        if stale >= args.conditional_patience:
            break
    if best_state is None:
        raise RuntimeError("conditional head produced no finite checkpoint")
    head.load_state_dict(best_state)
    head.eval()
    return {
        "history": history,
        "best_epoch": best_epoch,
        "best_validation_objective": best_objective,
        "target_temperature": temperature,
        "heteroscedastic_regression": "Gaussian NLL plus Smooth-L1",
        "ranking": (
            "pairwise logistic and listwise softmax only within groups "
            "sharing the same occupied store/query"
        ),
        "pair_deletion_supervision": bool(train_targets.pair_valid.any()),
    }


def centered_rank_correlation(
    prediction: np.ndarray,
    target: np.ndarray,
    valid: np.ndarray,
) -> float | None:
    prediction_rows = []
    target_rows = []
    for row in range(len(prediction)):
        mask = valid[row]
        if int(mask.sum()) < 2:
            continue
        pred = prediction[row, mask]
        truth = target[row, mask]
        prediction_rows.append(pred - pred.mean())
        target_rows.append(truth - truth.mean())
    if not prediction_rows:
        return None
    return rank_correlation(
        np.concatenate(prediction_rows), np.concatenate(target_rows)
    )


def pairwise_query_statistics(
    prediction: np.ndarray,
    target: np.ndarray,
    valid: np.ndarray,
) -> tuple[np.ndarray, np.ndarray]:
    correct = np.zeros(len(prediction), dtype=np.float64)
    count = np.zeros(len(prediction), dtype=np.float64)
    for row in range(len(prediction)):
        indices = np.flatnonzero(valid[row])
        for offset, first in enumerate(indices):
            for second in indices[offset + 1 :]:
                true_delta = target[row, first] - target[row, second]
                if abs(float(true_delta)) <= 1e-6:
                    continue
                pred_delta = prediction[row, first] - prediction[row, second]
                correct[row] += float(pred_delta * true_delta > 0)
                count[row] += 1.0
    return correct, count


def deletion_gap_by_query(
    prediction: np.ndarray,
    target: np.ndarray,
    valid: np.ndarray,
    seed: int,
) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    high = np.full(len(prediction), np.nan, dtype=np.float64)
    random_value = np.full(len(prediction), np.nan, dtype=np.float64)
    gap = np.full(len(prediction), np.nan, dtype=np.float64)
    rng = np.random.default_rng(seed)
    for row in range(len(prediction)):
        candidates = np.flatnonzero(valid[row])
        if len(candidates) < 2:
            continue
        high_slot = int(candidates[np.argmax(prediction[row, candidates])])
        random_slot = int(rng.choice(candidates))
        high[row] = target[row, high_slot]
        random_value[row] = target[row, random_slot]
        gap[row] = high[row] - random_value[row]
    return high, random_value, gap


def percentile_ci(
    values: np.ndarray,
    seed: int,
    repetitions: int = 2000,
) -> list[float | None]:
    finite = values[np.isfinite(values)]
    if not len(finite):
        return [None, None]
    if len(finite) == 1:
        return [float(finite[0]), float(finite[0])]
    rng = np.random.default_rng(seed)
    means = np.asarray(
        [
            rng.choice(finite, size=len(finite), replace=True).mean()
            for _ in range(repetitions)
        ]
    )
    return np.quantile(means, [0.025, 0.975]).astype(float).tolist()


def evaluate_predictions(
    prediction: np.ndarray,
    target: np.ndarray,
    valid: np.ndarray,
    seed: int,
) -> tuple[dict[str, Any], dict[str, np.ndarray]]:
    pooled = rank_correlation(prediction[valid], target[valid])
    centered = centered_rank_correlation(prediction, target, valid)
    correct, pair_count = pairwise_query_statistics(
        prediction, target, valid
    )
    high, random_value, gap = deletion_gap_by_query(
        prediction, target, valid, seed
    )
    total_pairs = float(pair_count.sum())
    finite_gap = gap[np.isfinite(gap)]
    metrics = {
        "pooled_spearman": pooled,
        "within_query_centered_spearman": centered,
        "pairwise_accuracy": (
            float(correct.sum() / total_pairs) if total_pairs else None
        ),
        "pair_count": int(total_pairs),
        "eligible_deletion_queries": int(len(finite_gap)),
        "high_conditional_effect": (
            float(np.nanmean(high)) if len(finite_gap) else None
        ),
        "random_conditional_effect": (
            float(np.nanmean(random_value)) if len(finite_gap) else None
        ),
        "high_minus_random": (
            float(np.nanmean(gap)) if len(finite_gap) else None
        ),
        "high_minus_random_query_bootstrap_ci95": percentile_ci(
            gap, seed + 81_119
        ),
    }
    arrays = {
        "prediction": prediction.astype(np.float32),
        "target": target.astype(np.float32),
        "valid": valid.astype(bool),
        "pair_correct": correct.astype(np.float32),
        "pair_count": pair_count.astype(np.float32),
        "high_effect": high.astype(np.float32),
        "random_effect": random_value.astype(np.float32),
        "deletion_gap": gap.astype(np.float32),
    }
    return metrics, arrays


